# 整形：没有小数点的数值就是整形
# 注意事项：如果一个整形的值放到单双引号中，那么它就是一个字符串类型，而不是整形
# apples = '4'
# print(apples) # 4
# print(type(apples)) # str

apples = 4
print(apples) # 4
print(type(apples)) # int
print('='*20)

# 浮点类型：带有小数点的数值
# 注意事项：如果一个浮点类型的数值放到单双引号中，那么它就是一个字符串类型，而不是浮点类型
price = 8.8
amount = 9.0
print(price) # 8.8
print(type(price)) # float
print(amount) # 9.0
print(type(amount)) # float
print('='*20)

# 字符串类型：使用单双引号的值
str1 = '9.0'
str2 = 'abcd'
print(str1) # 9.0
print(type(str1)) # str
print(str2) # abcd
print(type(str2)) # str

